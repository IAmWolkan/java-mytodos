package com.mytodos.Display;

public class MenuItem {
  private String text;
  private char key;

  public String getText() {
    return this.text;
  }

  public void setText(String text) {
    this.text = text;
  }

  public char getKey() {
    return this.key;
  }

  public void setKey(char key) {
    this.key = key;
  }
}
